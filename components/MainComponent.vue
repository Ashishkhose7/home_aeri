<script setup>
import { ref } from "vue";
import { onMounted } from "vue";

onMounted(() => {
  if (process.client) {
    // JavaScript Code to Clone Items
    let items = document.querySelectorAll(".carousel .carousel-item");

    items.forEach((el) => {
      const minPerSlide = 4;
      let next = el.nextElementSibling;
      for (var i = 1; i < minPerSlide; i++) {
        if (!next) {
          // Wrap carousel by using first child
          next = items[0];
        }
        let cloneChild = next.cloneNode(true);
        el.appendChild(cloneChild.children[0]);
        next = next.nextElementSibling;
      }
    });

    const swiper = new Swiper(".swiper-container", {
      slidesPerView: 2.5,
      spaceBetween: 10,
      centeredSlides: true,
      loop: true,
      autoplay: {
        delay: 3000000,
        disableOnInteraction: true,
      },
      breakpoints: {
        // Mobile Screens
        0: {
          slidesPerView: 1.2, // Show 1 slide on screens smaller than 640px
          spaceBetween: 70,
          centeredSlides: true, // Center the single slide

        },
        // Tablet Screens
        640: {
          slidesPerView: 2.5, // Show 2 slides on screens between 640px and 1024px
          spaceBetween: -40,
          centeredSlides: true, // Center the single slide

        },
        // Larger Screens
        1024: {
          slidesPerView: 2.5, // Maintain the existing configuration for larger screens
          spaceBetween: -70,
        },
      },
      pagination: {
        el: ".swiper-pagination",
        clickable: true,
      },
      slidesPerGroup: 2,
    });
  }
});
</script>

